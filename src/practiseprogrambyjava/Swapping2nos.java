package practiseprogrambyjava;

public class Swapping2nos {
	
	
public static void main (String args[])  {
	int a = 10, b=20 ;
	System.out.println("Before swapping values are : "+a+","+b);
	int t = a ;
	a = b ;
	b = t ;
	System.out.println("after swapping values are : "+a+","+b);
	
}
}
